# ============================================
# FUNCTION: Hyperopt Optimization
# ============================================
hyperopt_optimize() {
    echo ""
    echo -e "${BLUE}ğŸ¯ Starting Hyperopt Optimization...${NC}"
    echo -e "${YELLOW}âš ï¸  This may take several hours!${NC}"
    
    read -p "Enter number of epochs (default: 500): " epochs
    epochs=${epochs:-500}
    
    echo ""
    echo -e "${BLUE}Optimizing BUY parameters...${NC}"
    freqtrade hyperopt \
        --config $CONFIG \
        --strategy $STRATEGY \
        --hyperopt-loss SharpeHyperOptLoss \
        --spaces buy \
        --epochs $epochs \
        --timerange $TIMERANGE \
        --enable-protections
    
    echo ""
    echo -e "${BLUE}Optimizing SELL parameters...${NC}"
    freqtrade hyperopt \
        --config $CONFIG \
        --strategy $STRATEGY \
        --hyperopt-loss SharpeHyperOptLoss \
        --spaces sell \
        --epochs $((epochs / 2)) \
        --timerange $TIMERANGE \
        --enable-protections
    
    echo ""
    echo -e "${BLUE}Optimizing ROI parameters...${NC}"
    freqtrade hyperopt \
        --config $CONFIG \
        --strategy $STRATEGY \
        --hyperopt-loss SharpeHyperOptLoss \
        --spaces roi \
        --epochs 200 \
        --timerange $TIMERANGE \
        --enable-protections
    
    echo ""
    echo -e "${BLUE}Optimizing STOPLOSS parameters...${NC}"
    freqtrade hyperopt \
        --config $CONFIG \
        --strategy $STRATEGY \
        --hyperopt-loss SharpeHyperOptLoss \
        --spaces stoploss \
        --epochs 200 \
        --timerange $TIMERANGE \
        --enable-protections
    
    echo ""
    echo -e "${GREEN}âœ… Hyperopt optimization completed!${NC}"
    echo -e "${YELLOW}ğŸ“‹ Showing best results:${NC}"
    freqtrade hyperopt-list --best --no-details
}

