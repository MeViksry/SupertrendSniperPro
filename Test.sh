# ============================================
# FUNCTION: Webhook Test
# ============================================
test_webhook() {
    echo ""
    echo -e "${BLUE}ğŸ”— Testing Webhook Integration...${NC}"
    
    read -p "Enter your webhook URL: " webhook_url
    
    if [ -z "$webhook_url" ]; then
        echo -e "${RED}âŒ No webhook URL provided!${NC}"
        return 1
    fi
    
    # Update config with webhook URL
    jq --arg url "$webhook_url" '.webhook.url = $url' $CONFIG > temp.json && mv temp.json $CONFIG
    
    echo -e "${YELLOW}Testing webhook with sample data...${NC}"
    
    # Send test webhook
    curl -X POST "$webhook_url" \
        -H "Content-Type: application/json" \
        -d '{
            "action": "test",
            "pair": "BTC/USDT",
            "direction": "long",
            "price": 50000,
            "strategy": "SupertrendSniperPro",
            "timestamp": "'$(date -u +"%Y-%m-%dT%H:%M:%SZ")'"
        }'
    
    if [ $? -eq 0 ]; then
        echo ""
        echo -e "${GREEN}âœ… Webhook test successful!${NC}"
    else
        echo ""
        echo -e "${RED}âŒ Webhook test failed!${NC}"
    fi
}

